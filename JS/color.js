let color=prompt('Please pick a color and we will let you know if its dark or light').toLowerCase();

switch(color){
    case 'red':console.log('Light')  
        break
    case 'yellow':console.log('light')  
        break 
    case 'pink':console.log('light')  
        break
    case 'blue':console.log('dark')  
        break
    case 'purple':console.log('dark')  
        break
    case 'brown':console.log('dark')  
        break
    default:
        console.log('Unknown color strength')                       
}